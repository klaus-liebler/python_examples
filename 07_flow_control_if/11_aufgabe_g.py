# Schreiben Sie ein Python-Programm, 
# das „Hallo” ausgibt, wenn 1 in Variable “schnitzel” steht,  
# „Hallöchen“, wenn 2 
# und „Grüße“, wenn etwas anderes.

schnitzel=input("Geben Sie etwas ein!: ")
if schnitzel=="1":
    print("Hallo")
elif schnitzel=="2":
    print("Hallöchen")
else:
    print("Grüße")
